_ISO4217_INDEX: dict[str, int] = {c: i for i, c in enumerate(ACTIVE_ISO4217)}


def display_currency_index(iso: str) -> int:
    """Index for st.selectbox when `iso` is in the official list; else 0 (USD-first list)."""
    u = (iso or "").strip().upper()
    if len(u) != 3:
        return _ISO4217_INDEX.get("USD", 0)
    return _ISO4217_INDEX.get(u, _ISO4217_INDEX.get("USD", 0))
